    private void viewBtnAirlineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnAirlineActionPerformed
        int selectedRows=airlineListJTable.getSelectedRow();
        if(selectedRows<0){
            JOptionPane.showMessageDialog(null, "Please select a row from the table first" , "Warning" , JOptionPane.WARNING_MESSAGE);
            return;
        }
        else{
            Airliner a=(Airliner)airlineListJTable.getValueAt(selectedRows, 0);
            ViewAirlinerJPanel vajp= new ViewAirlinerJPanel(travelAgencyContainer,a,airlinerDirectory);
            travelAgencyContainer.add("ViewAirlinerJPanel",vajp);
            CardLayout layout = (CardLayout)travelAgencyContainer.getLayout();
            layout.next(travelAgencyContainer);
        }
    }//GEN-LAST:event_viewBtnAirlineActionPerformed

    private void removeBtnAirlineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnAirlineActionPerformed
        int selectedRow=airlineListJTable.getSelectedRow();
        if (selectedRow >= 0){
            int selectionButton= JOptionPane.YES_NO_OPTION;
            int selectionResult= JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning", selectionButton);
            if (selectionResult == JOptionPane.YES_OPTION){
                Airliner air = (Airliner)airlineListJTable.getValueAt(selectedRow, 0);
                airlinerDirectory.removeAirliner(air);
                refreshTable();
            }            
        }
        else{
                JOptionPane.showMessageDialog(null, "Please select a row!!");
        }
    }//GEN-LAST:event_removeBtnAirlineActionPerformed


