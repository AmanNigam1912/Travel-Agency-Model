    public void refreshTable(){
        int rowCount=airlineListJTable.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel)airlineListJTable.getModel();
        //dtm.setRowCount(0);
        for (int i = rowCount-1; i>=0; i-- ){
            dtm.removeRow(i);
        }
        
        for (Airliner a: airlinerDirectory.getAirlineDirectory()){
            Object row[]= new Object[1];
            row[0] = a;
            //row[1]=a.getAirlineName();
            dtm.addRow(row);
        }
    }
    
